package learningpackage;

public class Datatypes {

	public static void main(String[] args) {
		
		int number1=123;
		short number2=23;
		byte number3=12;
		long number4=1234545645645l;
		float number5=25.234f;
		char c='A';
		String Name="Athira";
		
		System.out.println("Integer number is "+number1);
		System.out.println("Short type number is "+number2);
		System.out.println("byte number is "+number3);
		System.out.println("long number is "+number4);
		System.out.println("float number is "+number5);
		System.out.println("char  is "+c);
		System.out.println("String number is "+Name);

	}

}
